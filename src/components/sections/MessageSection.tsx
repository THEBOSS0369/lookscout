"use client";
import { useEffect, useRef } from "react";

export function MessageSection() {
  const sectionRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          const target = entry.target as HTMLElement;
          if (entry.isIntersecting) {
            target.classList.add("fade-in-up");
            target.classList.remove("opacity-0");
          }
        });
      },
      {
        threshold: 0.2,
      },
    );

    const elements = sectionRef.current?.querySelectorAll(".fade-target");
    elements?.forEach((el) => {
      observer.observe(el);
    });

    return () => {
      elements?.forEach((el) => {
        observer.unobserve(el);
      });
    };
  }, []);

  return (
    <section className="py-20 bg-white" ref={sectionRef}>
      <div className="container mx-auto px-6">
        <div className="text-center mb-16 fade-target opacity-0 transition-all duration-700">
          <h2 className="text-4xl font-bold text-gray-900 mb-4">
            Messaging for all
          </h2>
          <p className="text-gray-600 text-lg max-w-2xl mx-auto">
            User generated content in real-time will have multiple touchpoints
            for offshoring
          </p>
        </div>

        <div className="grid md:grid-cols-2 lg:grid-cols-3 gap-8">
          {features.map((feature, idx) => (
            <div
              key={idx}
              className="text-center card-hover fade-target opacity-0 transition-all duration-700 delay-[200ms]"
              style={{ transitionDelay: `${idx * 100 + 200}ms` }}
            >
              <div className="w-16 h-16 bg-blue-600 rounded-full flex items-center justify-center mx-auto mb-6">
                {feature.icon}
              </div>
              <h3 className="text-xl font-bold text-gray-900 mb-3">
                {feature.title}
              </h3>
              <p className="text-gray-600">{feature.description}</p>
            </div>
          ))}
        </div>
      </div>

      <style jsx>{`
        .fade-in-up {
          opacity: 1 !important;
          transform: translateY(0) !important;
        }

        .fade-target {
          transform: translateY(20px);
        }
      `}</style>
    </section>
  );
}

